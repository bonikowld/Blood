          <h3 align="center">How to Donate Blood</h3>
          
          <h4>Preparation before donating blood</h4>
          <ul>
            <li>Have enough rest and sleep.</li>
            <li>No alcohol intake 24 hours prior to blood donation.</li>
            <li>No medications for at least 24 hours prior to blood donation.</li>
            <li>Have something to eat prior to blood donation, avoid fatty food.</li>
            <li>Drink plenty of fluid, like water or juice.</li>
          </ul>
          <hr>
          <h4>Steps in donating blood</h4>
          <ul>
            <li>Have your weight taken.</li>
            <li>Register and honestly and complete the donor registration form.</li>
            <li>Have your blood type and hemoglobin checked.</li>
            <li>A physician will conduct a blood donor examination.</li>
            <li>Actual donation--the amount of blood to be donated (either 350cc or 450cc) will depend on the 
                donors weight and blood pressure. It usually takes 10 minutes or less.</li>
            <li>A 5 to 10 minute rest and plenty of fluid-intake are necessary after donation.</li>
          </ul>
          <hr>
          <h4>What to do after blood donation?</h4>
          <ul>
            <li>Drink plenty of fluid, like water or juice.</li>
            <li>Refrain from stooping down after blood donation.</li>
            <li>Refrain from strenuous activities like: Lifting heavy objects, Driving big vehicles such as bus, 
                trucks, etc or Operating big machines.</li>
            <li>Avoid using the punctured arm in lifting heavy objects.</li>
            <li>Apply pressure on the punctured site and lift the arm in case the site is still bleeding.</li>
            <li>If there is discoloration and swelling on the punctured site, you may apply cold compress for 24 hours.</li>
            <li>If there is dizziness, just lie down with your feet elevated. Drink plenty of juice; and in just a few minutes or so, it will pass.</li>
          </ul>
          <hr>
          <h4>Blood Donation: Basic Requirements</h4>
              <p>Blood donation helps save lives. Find out if you are 
                eligible to donate blood and what to expect if you decide to donate.
              </p>
              <p align="justify">
              The need for blood is great. On any given day, more than two thousand of blood units 
              are transfused to patients in our country. Some may need blood during surgery; while
              others depend on it after an accident or because they have a disease that requires blood
              components. The Philippine Red Cross approximately supplies one-fourth of the countries 
              national blood requirements.If you are in good health, you can probably donate. Find out
              about blood donation and what to expect if you decide to give.
              </p>
          <hr>
          <h4>Who can donate blood?</h4>
          <ul>
            <li>Are in good health</li>
            <li>Are between 16 to 65 years old (16 and 17 years old need parents consent)</li>
            <li>Weigh at least 110 pounds</li>
            <li>Have a blood pressure between: Systolic: 90-160 mmHg, Diastolic: 60-100 mmHg</li>
            <li>Pass the physical and health history assessments.</li>
          </ul>

