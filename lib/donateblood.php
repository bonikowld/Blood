          <h3 align="center">FAQs</h3>

          <h4>What happens to my donated blood?</h4>
          <p align="justify">
          Each unit of blood collected will be examined for 5 transfusion-transmissible infectious diseases,
          namely: HIV, Malaria, Syphillis, Hepatitis B, and Hepatitis C before it is transfused to patients.
          </p>
          <hr>
          <h4>Is it safe to give blood?</h4>
          <p align="justify">
          Yes. The Red Cross ensures that donating blood is a safe opportunity to give the gift of life. Each
          needle used in the procedure is sterile and is disposed after a single use. It is important that all
          blood donors are in good health, well-rested, and have eaten prior to donation.
          </p>
          <hr>
          <h4>When can we donate blood?</h4>
          <p>
          A healthy person may donate blood every three months.
          </p>
          <hr>
          <h4>Where can I donate blood?</h4>
          <p align="justify">
          You may come and visit PRCs National Blood Center, Regional Blood Centers, or any of its Blood Services
          Facilities, nationwide.
          </p>
          <hr>
          <h4>Can a person who has a tattoo donate blood?</h4>
          <p align="justify">
          As long as the tattooing procedure was done aseptically (in a sterile manner), he/ she may donate blood 
          one year after the procedure. This is the same with ear piercing, acupuncture, and other procedures involving
          needles.
          </p>
          <hr>
          <h4>Are the health history questions necessary every time I donate?</h4>
          <p align="justify">
          To ensure the safest possible blood supply, all donors must undergo the necessary screening every donation. The
          World Health Organization and the Department of Health require all blood centers to conform to this practice.
          </p>
          <hr>
          <h4>What does the term "donor deferral" mean?</h4>
          <p align="justify">
          Individuals disqualified from donating blood are known as "deferred" donors. A prospective donor may be 
          deferred at any point during the collection and testing process. Whether or not a person is deferred,
          temporarily or permanently, will depend on the specific reason for disqualification (i.e. a person may be
          deferred temporarily because of anemia, a condition that is usually reversible). If a person is to be deferred,
          his or her name is entered into a list of deferred donors maintained by the blood center, often known as the
          "deferral registry." If a deferred donor attempts to give blood before the end of the deferral period, the donor
          will not be accepted for donation. Once the reason for the deferral no longer exists and the temporary deferral
          period has lapsed, the donor may return to the blood bank and be re-entered into the system.
          </p>
          <hr>
          <h4>If I was deferred once before, am I still ineligible to donate?</h4>
          <p align="justify">
          If your deferral is of a permanent nature, you will be informed. Otherwise, the deferral time depends upon the
          reason for deferral. Prior to each donation, you will be given a mini-physical and medical interview. At that
          time, it will be determined if you are eligible to donate blood on that particular day.
          </p>
          <hr>
          <h4>What are some of the reasons for permanent deferral?</h4>
          <p>- Hepatitis B or C infection.</p>
          <p>- HIV infection.</p>
          <p>- Having sexual contact with a person infected with HIV</p>
          <p>- Having multiple sex partners/ patronizing sex workers</p>
          <p>- Serious chronic illness (heart and lung diseases)</p>
          <hr>
          <h4>Can a person who just had his/ her tooth extracted donate blood?</h4>
          <p>
          He/ She will be temporarily deferred for a year.
          </p>
          <hr>
          <h4>If I just received a flu shot, can I donate blood?</h4>
          <p>
          Yes. There is no waiting period to donate after receiving a flu shot.
          </p>
          <hr>
          <h4>If I have a cold flu, can I donate blood?</h4>
          <p align="justify">
          In order to donate, blood centers require that you should be generally in good health (symptom-free); thus, it is
          important that you are feeling well.
          </p>
          <hr>
          <h4>Can I still donate if I have high blood pressure?</h4>
          <p>
          Yes, if your blood pressure is under control and within the limits set in the donation guidelines.
          </p>
          <hr>
          <h4>What if I am taking aspirin or medication prescribed by my doctor?</h4>
          <p align="justify">
          Aspirin and Ibuprofen will not affect a whole blood donation. Apheresis platelet donors, however, must not
          take aspirin or aspirin products 36 hours prior to the donation. Many other medications are acceptable; but
          it is recommended that you call the blood center ahead of time to inquire about the type of medication you
          are taking.
          </p>
          <hr>
          <h4>What if I have Anemia?</h4>
          <p align="justify">
          You cannot give blood if you have anemia. However, this can often be a temporary condition. Your hemoglobin
          level will be tested before you donate, in order to make sure that it is within an acceptable range.
          </p>
          <hr>
          <h4>How long does it take to donate blood?</h4>
          <p>
          The whole process of donating blood will only take an average of 25 minutes.
          </p>
          <hr>
          <h4>Will I put on weight after blood donation?</h4>
          <p>
          No. All you put on is the feeling of satisfaction because you have helped someone.
          </p>
          <hr>
          <h4>What other types of tests are done on the blood?</h4>
          <p align="justify">
          Your blood is tested to determine your blood type—classified as A, B, AB, and O—and your Rh factor.
          The Rh factor refers to the presence or absence of a specific antigen, a substance capable of stimulating an
          immune response, in the blood; so, you are either Rh positive or Rh negative, meaning you either carry the
          antigen or you do not. This information is important to know, because your blood type and Rh factor must be
          compatible with the blood type and Rh factor of the person receiving your blood.
          </p>
          <hr>
          <h4>What is the most common blood type?</h4>
          <p align="justify">
          The approximate distribution of blood types in the Philippines population is as follows (though distribution 
          may be different for specific racial and ethnic groups):
          </p>
          <p>O Rh-positive --- 44-46 percent</p>
          <p>A Rh-positive --- 22-23 percent</p>
          <p>B Rh-positive --- 24-25 percent</p>
          <p>AB Rh-positive --- 4-6 percent</p>
          <p>Rh-negative group --- Less than 1 percent</p>
          <hr>
          <h4>What fees are associated with blood?</h4>
          <p align="justify">
          While the donated blood is free, there are significant costs associated with the collection, testing, 
          labeling, preparation of components, and storage of blood. In addition to these, charges are also incurred
          through recruitment and education of donors, as well as quality assurance. As a result, processing fees are
          charged to recover these costs. Blood processing fees collected are in conformance with the stipulated 
          allowable fees as mandated by the Department of Health.
          </p>
          <hr>
          <h4>What can you do if you are not eligible to donate?</h4>
          <p align="justify">
          While a given individual may be unable to donate, he or she may be able to recruit a suitable donor. 
          PRC Blood Banks are always in need of volunteers to assist during blood donations, or to organize mobile 
          blood drives. In addition, monetary donations through the Blood Samaritan Project of the Red Cross are always
          welcome, to help ensure that blood banks can continue providing safe blood to those in need, most 
          especially to indigent patients.
          </p>
          <hr>
          <h4>How can I host a mobile blood donation activity at work, school, church or community?</h4>
          <p align="justify">
          Kindly refer to the Blood Services Facility near you. Contact the blood center in order to learn more about
          the requirements.
          </p>
